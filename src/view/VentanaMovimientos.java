    private void iniciarTabla() {
        String[] columnas = {"Tipo", "Fecha", "Hora", "Matr√≠cula"};
        DefaultTableModel modelo = new DefaultTableModel(columnas, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        jTableTablaMovimientos.setModel(modelo);
    }
    
    private void actualizarMovimientos() {
        DefaultTableModel modelo = (DefaultTableModel) jTableTablaMovimientos.getModel();
        modelo.setRowCount(0);

        for (int i = 0; i < movimientos.size(); i++) {
            Object movimiento = movimientos.get(i);

            String tipo = "";
            String fecha = "";
            String hora = "";
            String matricula = "";

            if (movimiento instanceof Entrada) {
                Entrada entrada = (Entrada) movimiento;
                tipo = "ENTRADA";
                fecha = entrada.getFecha();
                hora = entrada.getHora();
                matricula = entrada.getVehiculo().getMatricula();
            } else if (movimiento instanceof Salida) {
                Salida salida = (Salida) movimiento;
                tipo = "SALIDA";
                fecha = salida.getFecha();
                hora = salida.getHora();
                matricula = salida.getVehiculo().getMatricula();
            } else if (movimiento instanceof ServicioAdicional) {
                ServicioAdicional servicio = (ServicioAdicional) movimiento;
                tipo = "SERVICIO";
                fecha = servicio.getFecha();
                hora = servicio.getHora();
                matricula = servicio.getVehiculo().getMatricula();
            }

            modelo.addRow(new Object[]{tipo, fecha, hora, matricula});
        }
    }

